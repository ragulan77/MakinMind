<?php

namespace MakinMind\ProjectBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * MakinMind\ProjectBundle\Entity\ApplyTerms
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="MakinMind\ProjectBundle\Entity\ApplyTermsRepository")
 */
class ApplyTerms
{
    /**
     * @var integer $id
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var smallint $pbStudies
     *
     * @ORM\Column(name="pbStudies", type="smallint")
     */
    private $pbStudies;

    /**
     * @var string $firstDiploma
     *
     * @ORM\Column(name="firstDiploma", type="string", length=70)
     */
    private $firstDiploma;

    /**
     * @var string $secondDiploma
     *
     * @ORM\Column(name="secondDiploma", type="string", length=70)
     */
    private $secondDiploma;

    /**
     * @var string $specialization
     *
     * @ORM\Column(name="specialization", type="string", length=50)
     */
    private $specialization;

    /**
     * @var smallint $numberWorkedYears
     *
     * @ORM\Column(name="numberWorkedYears", type="smallint")
     */
    private $numberWorkedYears;

    /**
     * @var smallint $hoursPerWeek
     *
     * @ORM\Column(name="hoursPerWeek", type="smallint")
     */
    private $hoursPerWeek;

    /**
     * @var boolean $paralleleContract
     *
     * @ORM\Column(name="paralleleContract", type="boolean")
     */
    private $paralleleContract;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set pbStudies
     *
     * @param smallint $pbStudies
     */
    public function setPbStudies($pbStudies)
    {
        $this->pbStudies = $pbStudies;
    }

    /**
     * Get pbStudies
     *
     * @return smallint 
     */
    public function getPbStudies()
    {
        return $this->pbStudies;
    }

    /**
     * Set firstDiploma
     *
     * @param string $firstDiploma
     */
    public function setFirstDiploma($firstDiploma)
    {
        $this->firstDiploma = $firstDiploma;
    }

    /**
     * Get firstDiploma
     *
     * @return string 
     */
    public function getFirstDiploma()
    {
        return $this->firstDiploma;
    }

    /**
     * Set secondDiploma
     *
     * @param string $secondDiploma
     */
    public function setSecondDiploma($secondDiploma)
    {
        $this->secondDiploma = $secondDiploma;
    }

    /**
     * Get secondDiploma
     *
     * @return string 
     */
    public function getSecondDiploma()
    {
        return $this->secondDiploma;
    }

    /**
     * Set specialization
     *
     * @param string $specialization
     */
    public function setSpecialization($specialization)
    {
        $this->specialization = $specialization;
    }

    /**
     * Get specialization
     *
     * @return string 
     */
    public function getSpecialization()
    {
        return $this->specialization;
    }

    /**
     * Set numberWorkedYears
     *
     * @param smallint $numberWorkedYears
     */
    public function setNumberWorkedYears($numberWorkedYears)
    {
        $this->numberWorkedYears = $numberWorkedYears;
    }

    /**
     * Get numberWorkedYears
     *
     * @return smallint 
     */
    public function getNumberWorkedYears()
    {
        return $this->numberWorkedYears;
    }

    /**
     * Set hoursPerWeek
     *
     * @param smallint $hoursPerWeek
     */
    public function setHoursPerWeek($hoursPerWeek)
    {
        $this->hoursPerWeek = $hoursPerWeek;
    }

    /**
     * Get hoursPerWeek
     *
     * @return smallint 
     */
    public function getHoursPerWeek()
    {
        return $this->hoursPerWeek;
    }

    /**
     * Set paralleleContract
     *
     * @param boolean $paralleleContract
     */
    public function setParalleleContract($paralleleContract)
    {
        $this->paralleleContract = $paralleleContract;
    }

    /**
     * Get paralleleContract
     *
     * @return boolean 
     */
    public function getParalleleContract()
    {
        return $this->paralleleContract;
    }
}